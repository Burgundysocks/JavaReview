package chap_01;

public class _02_DataType {
    //자료형
    public static void main(String[] args){
        System.out.println("안녕하세요"); //문자열  char도 있다
        System.out.println(1);          //순자 - 정수형
        System.out.println(-34);
        System.out.println(3.14);       //숫자 - 실수형
        System.out.println(true);       //boolean
        System.out.println(false);


        System.out.println(123 + 345); //468
        System.out.println(1.23 + 345); //346.23
        System.out.println("1" + 23);
    }
}
